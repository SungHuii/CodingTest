package boj.알고리즘;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class SugarDelivery {

  public static void main(String[] args) throws IOException {
    /*
    * 그리디 알고리즘
    * 시간 제한 : 1초 / 메모리 제한 : 128 MB
    * 문제
    * 상근이는 요즘 설탕공장에서 설탕을 배달하고 있다. 상근이는 지금 사탕가게에 설탕을 정확하게 N킬로그램을 배달해야 한다.
    * 설탕공장에서 만드는 설탕은 봉지에 담겨져 있다. 봉지는 3킬로그램 봉지와 5킬로그램 봉지가 있다.
    *
    * 상근이는 귀찮기 때문에, 최대한 적은 봉지를 들고 가려고 한다.
    * 예를 들어, 18킬로그램 설탕을 배달해야 할 때, 3킬로그램 봉지 6개를 가져가도 되지만,
    * 5킬로그램 3개와 3킬로그램 1개를 배달하면, 더 적은 개수의 봉지를 배달할 수 있다.
    *
    * 상근이가 설탕을 정확하게 N킬로그램 배달해야 할 때, 봉지 몇 개를 가져가면 되는지 그 수를 구하는 프로그램을 작성하시오.
    *
    * 입력
    * 첫째 줄에 N이 주어진다. (3 ≤ N ≤ 5000)
    *
    * 출력
    * 상근이가 배달하는 봉지의 최소 개수를 출력한다. 만약, 정확하게 N킬로그램을 만들 수 없다면 -1을 출력한다.
    * */

    BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
    int n = Integer.parseInt(br.readLine());
    
    int count = 0;
    
    while (n >= 0) {
      if (n % 5 == 0) {
        count += n / 5;
        System.out.println(count);
        return;
      }
      n -= 3;
      count++;
    }
    
    System.out.println(-1);
  }

  /*
   * 문제 레벨 : 실버4
   * 제한 시간 : 20분
   * 걸린 시간 : 15분
   * 해결 과정 :
   * 1차 시도
   * 처음에는 5kg 봉지로 최대한 많이 채우기 위해 N을 5로 나눈 몫을 먼저 구하고 나머지를 3으로 나누어 떨어지는지 확인.
   * 이 방식은 6kg(3kg * 2)의 경우 5로 나누면 몫이 1, 나머지가 1이 되어 3으로 나누어 떨어지지 않아 -1을 출력하는 오류가 있었음.
   * 또한 18kg 같이 5kg와 3kg을 섞어야 정답인 경우를 제대로 처리하지 못함.
   *
   * 2차 시도
   * 몫을 구한 뒤 나머지가 3으로 나누어 떨어지지 않으면 -1을 출력하고 종료하는 로직을 작성해봄.
   * 이 방법은 5kg 봉지 개수를 줄여가며 3kg으로 채우는 경우(백트래킹 개념)를 고려하지 못했다.
   *
   * 최종 해결
   * N이 5의 배수가 될 때까지 3kg 봉지를 하나씩 빼면서(count 증가) 확인하는 방식으로 변경.
   * 1. N이 5로 나누어떨어지면 몫을 더하고 종료한다.
   * 2. 그렇지 않으면 3kg을 빼고 count를 1 증가시킨다.
   * 3. N이 음수가 될 때까지 반복하며, 해결되지 않으면 -1을 출력한다.
   * 5kg 봉지를 최대한 많이 쓰면서도, 3kg 봉지로 무게를 맞추는 모든 경우를 순차적으로 탐색하는 그리디 알고리즘.
   */

}
